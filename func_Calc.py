# Function Calculator

def add_num(num1, num2):
    sum = int(num1) + int(num2)
    return sum

def sub_num(num1, num2):
    sum = int(num1) - int(num2)
    return sum

def div_num(num1, num2):
    sum = int(num1) / int(num2)
    return sum

def mul_num(num1, num2):
    sum = int(num1) * int(num2)
    return sum